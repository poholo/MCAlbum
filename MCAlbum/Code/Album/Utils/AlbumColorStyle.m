+ (UIColor *)colorWithImpressionType:(NSString *)type {
    if ([type isEqualToString:@"1"]) {
        return [AlbumColorStyle colorWithHexInt:0x9013FE];
    }
    if ([type isEqualToString:@"2"]) {
        return [AlbumColorStyle colorWithHexInt:0x1DC9A3];
    }
    if ([type isEqualToString:@"3"]) {
        return [AlbumColorStyle colorWithHexInt:0x4A90E2];
    }
    if ([type isEqualToString:@"4"]) {
        return [AlbumColorStyle colorWithHexInt:0xFEB513];
    }
    return [UIColor clearColor];
}

+ (UIColor *)alphaColorWithImpressionType:(NSString *)type {
    if ([type isEqualToString:@"1"]) {
        return [AlbumColorStyle colorWithHexInt:0x9013FE99];
    }
    if ([type isEqualToString:@"2"]) {
        return [AlbumColorStyle colorWithHexInt:0x1DC9A399];
    }
    if ([type isEqualToString:@"3"]) {
        return [AlbumColorStyle colorWithHexInt:0x4A90E299];
    }
    if ([type isEqualToString:@"4"]) {
        return [AlbumColorStyle colorWithHexInt:0xFEB51399];
    }
    return [UIColor clearColor];
}

+ (UIColor *)colorWithHexInt:(NSUInteger)rgbValue {
    return [UIColor colorWithRed:(CGFloat) (((float) ((rgbValue & 0xFF0000) >> 16)) / 255.0)
                           green:(CGFloat) (((float) ((rgbValue & 0xFF00) >> 8)) / 255.0)
                            blue:(CGFloat) (((float) (rgbValue & 0xFF)) / 255.0)
                           alpha:1.0];
}
